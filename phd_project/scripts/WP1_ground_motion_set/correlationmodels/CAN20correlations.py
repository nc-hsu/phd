labels = [("GM", "PGV", "None")] + \
         [("GM", "SA", t) for t in periods]

def can20_empirical_correlation_model(which: str):
    # which = "between-event", "within-event", "total"
    if which == "total": data = total_correlation_can20
    elif which == "between-event": data = between_event_correlation_can20
    elif which == "within-event": data = within_event_correlation_can20
    else:
        raise ValueError("'which' must be one of: "
                         "between-event', 'within-event' or 'total'")
    df = pd.DataFrame(data, 
                      index=pd.MultiIndex.from_tuples(labels),
                      columns=pd.MultiIndex.from_tuples(labels))
    return df